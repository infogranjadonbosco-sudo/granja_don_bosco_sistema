-- 1. TABLA DE USUARIOS
CREATE TABLE IF NOT EXISTS usuarios (
  id UUID REFERENCES auth.users ON DELETE CASCADE PRIMARY KEY,
  nombre_completo TEXT NOT NULL,
  rol TEXT NOT NULL DEFAULT 'colaborador',
  departamento TEXT,
  puede_editar BOOLEAN DEFAULT FALSE,
  puede_eliminar BOOLEAN DEFAULT FALSE,
  puede_agregar BOOLEAN DEFAULT FALSE,
  puede_inventario BOOLEAN DEFAULT FALSE,
  correo TEXT NOT NULL,
  
  foto_url TEXT,
  ultima_conexion TIMESTAMPTZ,
  creado_at TIMESTAMPTZ DEFAULT timezone('utc'::text, now()) NOT NULL
);

-- 2. TABLA DE PRODUCTOS
CREATE TABLE IF NOT EXISTS products (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  name TEXT NOT NULL,
  price DECIMAL(10,2) NOT NULL,
  category TEXT,
  stock INTEGER DEFAULT 0,
  unidad_medida TEXT DEFAULT 'Unidad',
  image_url TEXT,
  created_at TIMESTAMPTZ DEFAULT timezone('utc'::text, now()) NOT NULL
);

-- 3. TABLA DE TAREAS
CREATE TABLE IF NOT EXISTS tareas (
  id SERIAL PRIMARY KEY,
  nombre TEXT NOT NULL,
  descripcion TEXT,
  creado_at TIMESTAMPTZ DEFAULT timezone('utc'::text, now()) NOT NULL
);

-- 4. TABLA DE JORNADAS LABORALES
CREATE TABLE IF NOT EXISTS jornadas (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  usuario_id UUID REFERENCES usuarios(id) ON DELETE CASCADE,
  tipo TEXT NOT NULL,
  tarea_id INTEGER REFERENCES tareas(id),
  creado_at TIMESTAMPTZ DEFAULT timezone('utc'::text, now()) NOT NULL
);

-- 5. TABLA DE PEDIDOS/VENTAS
CREATE TABLE IF NOT EXISTS orders (
  id UUID DEFAULT gen_random_uuid() PRIMARY KEY,
  usuario_id UUID REFERENCES usuarios(id),
  total DECIMAL(10,2) NOT NULL,
  metodo_pago TEXT NOT NULL DEFAULT 'efectivo',
  status TEXT NOT NULL DEFAULT 'completado',
  created_at TIMESTAMPTZ DEFAULT timezone('utc'::text, now()) NOT NULL
);

-- 6. TABLA DE MENSAJES DE CHAT
CREATE TABLE IF NOT EXISTS mensajes_chat (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  usuario_id UUID REFERENCES usuarios(id) ON DELETE CASCADE,
  nombre_usuario TEXT NOT NULL,
  mensaje TEXT NOT NULL,
  rol_usuario TEXT DEFAULT 'colaborador',
  grupo_id BIGINT REFERENCES grupos_chat(id) ON DELETE SET NULL,
  creado_at TIMESTAMPTZ DEFAULT timezone('utc'::text, now()) NOT NULL
);

-- 7. TABLA DE GRUPOS DE CHAT
CREATE TABLE IF NOT EXISTS grupos_chat (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  nombre TEXT NOT NULL,
  creador_id UUID REFERENCES usuarios(id) ON DELETE SET NULL,
  miembros UUID[] DEFAULT '{}',
  creado_at TIMESTAMPTZ DEFAULT timezone('utc'::text, now()) NOT NULL
);

