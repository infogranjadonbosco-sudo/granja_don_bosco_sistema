-- Políticas para usuarios
CREATE POLICY "Usuarios pueden ver todos los perfiles" ON usuarios FOR SELECT TO authenticated USING (true);
CREATE POLICY "Usuarios pueden actualizar su propio perfil" ON usuarios FOR UPDATE TO authenticated USING (auth.uid() = id);

-- Políticas para productos
CREATE POLICY "Todos pueden ver productos" ON products FOR SELECT TO authenticated USING (true);
CREATE POLICY "Admins pueden insertar productos" ON products FOR INSERT TO authenticated WITH CHECK (
  (SELECT rol FROM usuarios WHERE id = auth.uid()) = 'admin'
);
CREATE POLICY "Admins pueden actualizar productos" ON products FOR UPDATE TO authenticated USING (
  (SELECT rol FROM usuarios WHERE id = auth.uid()) = 'admin'
);
CREATE POLICY "Admins pueden eliminar productos" ON products FOR DELETE TO authenticated USING (
  (SELECT rol FROM usuarios WHERE id = auth.uid()) = 'admin'
);

-- Políticas para tareas
CREATE POLICY "Todos pueden ver tareas" ON tareas FOR SELECT TO authenticated USING (true);
CREATE POLICY "Admins pueden gestionar tareas" ON tareas FOR ALL TO authenticated USING (
  (SELECT rol FROM usuarios WHERE id = auth.uid()) = 'admin'
);

-- Políticas para jornadas
CREATE POLICY "Usuarios pueden ver sus jornadas" ON jornadas FOR SELECT TO authenticated USING (auth.uid() = usuario_id);
CREATE POLICY "Usuarios pueden crear jornadas" ON jornadas FOR INSERT TO authenticated WITH CHECK (auth.uid() = usuario_id);

-- Políticas para orders
CREATE POLICY "Todos pueden ver orders" ON orders FOR SELECT TO authenticated USING (true);
CREATE POLICY "Usuarios pueden crear orders" ON orders FOR INSERT TO authenticated WITH CHECK (true);

-- Políticas para chat
CREATE POLICY "Todos pueden ver mensajes" ON mensajes_chat FOR SELECT TO authenticated USING (true);
CREATE POLICY "Usuarios pueden enviar mensajes" ON mensajes_chat FOR INSERT TO authenticated WITH CHECK (auth.uid() = usuario_id);

-- Políticas para grupos_chat
CREATE POLICY "Todos pueden ver grupos" ON grupos_chat FOR SELECT TO authenticated USING (true);
CREATE POLICY "Usuarios pueden crear grupos" ON grupos_chat FOR INSERT TO authenticated WITH CHECK (auth.uid() = creador_id);

